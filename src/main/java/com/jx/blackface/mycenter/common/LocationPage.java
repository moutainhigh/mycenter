package com.jx.blackface.mycenter.common;

public class LocationPage {
	
	private String CommonCatalogue;  //指定的目录
	private String CommonLocation;   //指定的页面
	private String menuClass;       //指定的菜单样式
	
	public LocationPage(){}
	public LocationPage(String catalogue,String location,String menuClass){
		this.CommonCatalogue = catalogue;
		this.CommonLocation = location;
		this.menuClass = menuClass;
	}
	
	
	public String getCommonCatalogue() {
		return CommonCatalogue;
	}
	public void setCommonCatalogue(String commonCatalogue) {
		CommonCatalogue = commonCatalogue;
	}
	public String getCommonLocation() {
		return CommonLocation;
	}
	public void setCommonLocation(String commonLocation) {
		CommonLocation = commonLocation;
	}
	public String getMenuClass() {
		return menuClass;
	}
	public void setMenuClass(String menuClass) {
		this.menuClass = menuClass;
	}
	
}
